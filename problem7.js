export const permutation = (array) => {
  if (array.length === 0) return [[]];
  const result = [];
  for (let i = 0; i < array.length; i++) {
    const num = array[i];
    const restPerm = permutation(array.filter((_, j) => i !== j));
    for (const permute of restPerm) {
      result.push([num, ...permute]);
    }
  }

  return result;
};

